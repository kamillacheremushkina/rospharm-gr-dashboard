export const formatDate = (dateStr) => {
  if (!dateStr) return '';
  const [year, month, day] = dateStr.split('-');
  return `${day}.${month}.${year}`;
};

export const getGoalTasks = (goalId) => {
  return tasksData
    .filter(task => task.goal_id === goalId)
    .sort((a, b) => a.task_order - b.task_order);
};

export const getCompletedTasksCount = (tasks) => {
  return tasks.filter(task => task.task_status === 'Выполнено').length;
};

export const getProgressPercent = (tasks) => {
  if (!tasks.length) return 0;
  return Math.round((getCompletedTasksCount(tasks) / tasks.length) * 100);
};

export const getBlockProgress = (blockName) => {
  const blockGoals = goalsData.filter(goal => goal.gr_block === blockName);
  const blockGoalIds = blockGoals.map(goal => goal.goal_id);
  const blockTasks = tasksData.filter(task => blockGoalIds.includes(task.goal_id));

  if (!blockTasks.length) return 0;

  const completed = getCompletedTasksCount(blockTasks);
  return Math.round((completed / blockTasks.length) * 100);
};

export const getOverallProgress = () => {
  if (!tasksData.length) return 0;
  const completed = getCompletedTasksCount(tasksData);
  return Math.round((completed / tasksData.length) * 100);
};

export const getRiskLevel = (goal) => {
  if (goal.goal_status === 'В процессе') return 'Средний';
  if (goal.goal_status === 'Не начато') return 'Низкий';
  return 'Низкий';
};

export const getRiskImpact = (goal) => {
  if (goal.gr_block === 'Работа с госзаказчиками') return 'Высокое';
  if (goal.gr_block === 'Градостроительство и экология') return 'Высокое';
  return 'Среднее';
};

export const getResponseStatus = (goal) => {
  if (goal.goal_status === 'В процессе') return 'В работе';
  if (goal.goal_status === 'Не начато') return 'Не начато';
  return 'Закрыто';
};

